package admin.po;

public class SpeacValue{
private Long id;
private String name;
private Long speacId;
private Long sort;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Long getSpeacId() {
        return speacId;
    }

    public void setSpeacId(Long speacId) {
        this.speacId = speacId;
    }

    public Long getSort() {
        return sort;
    }

    public void setSort(Long sort) {
        this.sort = sort;
    }
}
